public class Hooray {                           //class declaration

    public static void hooray() {               //method creation
        while(true) {                           //while loop with boolean condition of "true"
            System.out.println("Hooray!");
        }
    }

    public static void main(String[] args) {    //main method
        hooray();                               //method call within class(does not require dot operator)
    }

}
